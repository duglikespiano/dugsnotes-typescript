/**
 * TypeScript return type summary:
 *
 * - `void`: Function returns but does not return a meaningful value.
 *    - Can return `undefined` or no value at all.
 *    - Example: Logging, side-effects.
 *
 * - `never`: Function never returns (no normal completion).
 *    - Used for functions that always throw errors or run infinite loops.
 *    - Cannot have a `return` statement.
 */
